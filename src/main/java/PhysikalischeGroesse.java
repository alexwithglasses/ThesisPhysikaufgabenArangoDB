public class PhysikalischeGroesse {

    private String bezeichnung;
    private String symbol;
    private String standardEinheit;
    private String standardEinheitName;

    public String getBezeichnung() {
        return bezeichnung;
    }

    public void setBezeichnung(String bezeichnung) {
        this.bezeichnung = bezeichnung;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getStandardEinheit() {
        return standardEinheit;
    }

    public void setStandardEinheit(String standardEinheit) {
        this.standardEinheit = standardEinheit;
    }

    public String getStandardEinheitName() {
        return standardEinheitName;
    }

    public void setStandardEinheitName(String standardEinheitName) {
        this.standardEinheitName = standardEinheitName;
    }

}
